import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);

const EN_PATH = path.join(__dirname, '../src/locales/locales/en.json');
const OUT_PATH = path.join(__dirname, '../src/locales/schema.d.ts');

function flattenKeys(obj, prefix = '') {
    let keys = [];
    for (const key in obj) {
        if (typeof obj[key] === 'object' && obj[key] !== null) {
            keys = keys.concat(flattenKeys(obj[key], prefix + key + '.'));
        } else {
            keys.push(prefix + key);
        }
    }
    return keys;
}

try {
    console.log('ğŸ“– Reading en.json...');
    const content = fs.readFileSync(EN_PATH, 'utf-8');
    const json = JSON.parse(content);

    console.log('ğŸ”¨ Flattening keys...');
    const keys = flattenKeys(json);

    // Create union type string
    const unionType = keys.map(k => `  | "${k}"`).join('\n');

    const fileContent = `// Auto-generated by scripts/generate-i18n-types.js
// Do not edit manually

export type TranslationKey =
${unionType};
`;

    console.log(`ğŸ’¾ Writing types to ${OUT_PATH}...`);
    fs.writeFileSync(OUT_PATH, fileContent);
    console.log(`âœ… Generated ${keys.length} translation keys.`);

} catch (err) {
    console.error('âŒ Error generating types:', err);
    process.exit(1);
}
